#include "libft.h"

/*
** +-------------lst------------+              +--------------+-------------+
** |              |             |            \ |              |             |
** |   CONTENT    |     NEXT    |--------------|   CONTENT    |     NEXT    |
** |              |             |            / |              |             |
** +--------------+-------------+              +--------------+-------------+
*/

t_list	*ft_lstlast(t_list *lst)
{
	if (lst == NULL)
		return (NULL);
	while (1)
	{
		if (lst->next == NULL)
			return (lst);
		lst = lst->next;
	}
}
