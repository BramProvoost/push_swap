#include "libft.h"

/*
** +--------------+-------------+              +--------------+-------------+
** |              |             |            \ |              |             |
** |   CONTENT    |     NEXT    |--------------|   CONTENT    |     NEXT    |
** |              |             |            / |              |             |
** +--------------+-------------+              +--------------+-------------+
*/

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	while (lst)
	{
		(*f)(lst->content);
		if (lst->next != NULL)
			lst = lst->next;
		else
			return ;
	}
}
